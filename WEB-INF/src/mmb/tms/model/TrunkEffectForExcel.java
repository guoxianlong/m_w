package mmb.tms.model;

public class TrunkEffectForExcel {
	
	private int index;
	private String trunkName;
	private String deliverAdminName;
	private String stockArea;
	private String deliverName;
	private String mode;
	private String time;
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public String getTrunkName() {
		return trunkName;
	}
	public void setTrunkName(String trunkName) {
		this.trunkName = trunkName;
	}
	public String getDeliverAdminName() {
		return deliverAdminName;
	}
	public void setDeliverAdminName(String deliverAdminName) {
		this.deliverAdminName = deliverAdminName;
	}
	public String getStockArea() {
		return stockArea;
	}
	public void setStockArea(String stockArea) {
		this.stockArea = stockArea;
	}
	public String getDeliverName() {
		return deliverName;
	}
	public void setDeliverName(String deliverName) {
		this.deliverName = deliverName;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
