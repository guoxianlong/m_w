package mmb.stock.stat;

public class ProductWarePropertyLogBean {
	
	public int id;
	public String operDetail;
	public int operId;
	public String operName;
	public int productWarePropertyId;
	public String time;
	
	
	
	public int getId() {
		return id;
	}
	public String getOperDetail() {
		return operDetail;
	}
	public int getOperId() {
		return operId;
	}
	public String getOperName() {
		return operName;
	}
	public int getProductWarePropertyId() {
		return productWarePropertyId;
	}
	public String getTime() {
		return time;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setOperDetail(String operDetail) {
		this.operDetail = operDetail;
	}
	public void setOperId(int operId) {
		this.operId = operId;
	}
	public void setOperName(String operName) {
		this.operName = operName;
	}
	public void setProductWarePropertyId(int productWarePropertyId) {
		this.productWarePropertyId = productWarePropertyId;
	}
	public void setTime(String time) {
		this.time = time;
	}

}
